        0x73: 0x4A, 0x74: 0x4B, 0x75: 0x4C, 0x77: 0x4D,
        0x79: 0x4E, 0x72: 0x75,
        0x52: 0x62, 0x53: 0x59, 0x54: 0x5A, 0x55: 0x5B,
