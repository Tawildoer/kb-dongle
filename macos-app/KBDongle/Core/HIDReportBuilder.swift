        0x5B: 0x60, 0x5C: 0x61, 0x41: 0x63, 0x43: 0x55,
        0x45: 0x57, 0x4B: 0x54, 0x4C: 0x58, 0x4E: 0x56,
    ]

    private static let modifierMap: [CGKeyCode: UInt8] = [
        0x3B: 0x01, 0x38: 0x02, 0x3A: 0x04, 0x37: 0x08,
        0x3E: 0x10, 0x3C: 0x20, 0x3D: 0x40, 0x36: 0x80,
    ]

    func keyDown(cgKeyCode: CGKeyCode, flags: CGEventFlags) -> [UInt8] {
        if let modBit = Self.modifierMap[cgKeyCode] {
            modifiers |= modBit
        } else if let hid = Self.keyMap[cgKeyCode], !pressedKeys.contains(hid) {
            if pressedKeys.count < 6 { pressedKeys.append(hid) }
        }
        return buildReport()
    }

    func keyUp(cgKeyCode: CGKeyCode, flags: CGEventFlags) -> [UInt8] {
        if let modBit = Self.modifierMap[cgKeyCode] {
            modifiers &= ~modBit
        } else if let hid = Self.keyMap[cgKeyCode] {
            pressedKeys.removeAll { $0 == hid }
        }
        return buildReport()
    }

    static func mouseReport(buttons: UInt8, dx: Int8, dy: Int8, scroll: Int8) -> [UInt8] {
        return [buttons, UInt8(bitPattern: dx), UInt8(bitPattern: dy), UInt8(bitPattern: scroll)]
    }

    private func buildReport() -> [UInt8] {
        var report = [UInt8](repeating: 0, count: 8)
        report[0] = modifiers
        report[1] = 0x00
        for (i, key) in pressedKeys.prefix(6).enumerated() {
            report[2 + i] = key
        }
        return report
    }
}
